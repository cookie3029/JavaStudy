package day02;

public class Test16 {
	public static void main(String[] args) {

		int a = 20;
		boolean b = true;
		if (b) {
			System.out.println("a는 10보다 크다.");
		}
		System.out.println("실행끝");

		if (a - 10 > 10) {
			System.out.println("hello");
		}

		int score = 70;
		// 점수가 60점 이상이면 "합격", 아니면 "불합격"
		if (score >= 60) {
			System.out.println("합격");
		}
		if (score < 60) {
			System.out.println("불합격");
		}

		if(score >= 60) {
			System.out.println("합격");
		} else {
			System.out.println("불합격");
		}
	}
}
